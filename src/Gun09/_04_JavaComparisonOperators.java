package Gun09;

public class _04_JavaComparisonOperators {
    public static void main(String[] args) {
        int a=50;
        int b=50;

        System.out.println(" a > b " + ( a > b)); // false : a, b den büyük mü
        System.out.println(" a >= b " + ( a >= b)); // true : a, b den büyük eşit mi
        System.out.println(" a < b " + ( a < b)); // false : a, b den küçük mü
        System.out.println(" a <= b " + ( a <= b)); // true : a, b den küçük eşit mi
        System.out.println(" a == b " + ( a == b)); // true : a, b ye eşit mi
        System.out.println(" a != b " + ( a != b)); // false : a, b den farklı mı
    }
}
