package Gun09;

public class _03_JavaAritmeticOperators {
    public static void main(String[] args) {
        int a=10;    // a nın değeri 10

        a=5;         // a nın değeri 5

        a=a+6;       // a nın değerini 6 artır : 11
        a+=6;        // a nın artış değeri 6   : 17

        a=a+1;       // a yı 1 artır : 18
        a++;         // a yı 1 artır : 19
        ++a;         // a yı 1 artır : 20

        a=a-5;       // a yı 5 azalt : 15
        a-=5;        // a yı 5 azalt : 10

        a=a*2;       // a yı 2 ile çarp : 20
        a*=2;        // a nın çarpım değeri 2 dir : 40;

        a=a/5;       // a yı 5 böl :  8
        a/=2;        // a nın bölüm değeri 2 : 4
    }
}
