package Gun09;

public class _01_JavaAritmeticOperators {
    public static void main(String[] args) {
        int sayac = 0;

        sayac = sayac + 1;   // sayacın değerini 1 artırır : 1
        sayac++;             // sayacın değerini 1 artırır : 2
        ++sayac;             // sayacın değerini 1 artırır : 3

        sayac = sayac - 1;   // sayacın değerini 1 azalt : 2
        sayac--;             // sayacın değerini 1 azalt : 1
        --sayac;             // sayacın değerini 1 azalt : 0
    }
}
