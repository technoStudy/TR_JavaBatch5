package Gun08;

public class _10_JavaAritmeticOperators {
    public static void main(String[] args) {
         int sayac=0;

         sayac=sayac+1 ;   // sayacın değerini 1 artır : 1
         sayac++;          // sayacın değerini 1 artır : 2
         ++sayac;          // sayacın değerini 1 artır : 3

         sayac=sayac-1 ;   // sayacın değerini 1 azaltır : 2
         sayac--;          // sayacın değerini 1 azaltır : 1
         --sayac;          // sayacın değerini 1 azaltır : 0










    }
}
